def random_10_var3(input_array):  
    
    try:
        
        # Обозначим количество итераций в переменную
        all_i = 300
        
        # В течение каждой итерации выбираются 10 чисел из входного массива
        for i in range(all_i):
            random_number_array = np.random.choice(input_array, size = 10)
            
            # Проверим принадлежность, всех полученных 10 чисел, диапазону (50;80)
            comparison = map(lambda number: 50 < number < 80, random_number_array)
            if all(comparison) is True:
            
                # Если условия задачи удовлетворены, функция возвращает массив с полученными числами 
                return list(np.array(random_number_array, dtype=int))

        # Если все итерации произведены, а условия задачи неудовлетворены скрипт кидает вручную созданный Exception
        raise Exception       

    except Exception:
        print("Не удалось набрать массив, включающий 10 чисел, находящихся в промежутке от 50 до 80")

res_3 = random_10_var3(input_array)

print(res_3)

